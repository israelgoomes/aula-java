public class Calculadora {
    
    public int soma(int vl1, int vl2){
        return  vl1 + vl2;
    }
    
    public int subtracao(int vl1, int vl2){
        return  vl1 - vl2;
    }
    
    public int multiplicacao(int vl1, int vl2){
        return  vl1 * vl2;
    }
    
    public int divisao(int vl1, int vl2){
        return  vl1 / vl2;
    }
}
